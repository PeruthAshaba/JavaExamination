package Box;

public class Box {
    //attribute of the box class
    private float width;

    //constructor of the box class

    public Box(float width) {
        this.width = width;
    }
    //Getters and setters

    public float getWidth() {
        return width;
    }

    public void setWidth(float width) {
        this.width = width;
    }


}
